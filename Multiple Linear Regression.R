########################################################################################################
#################################### I> INSTALL PACKAGES ###############################################
########################################################################################################

install.packages('caTools')

########################################################################################################
#################################### II> LOAD THE LIBRARIES ############################################
########################################################################################################

library(caTools)
set.seed(123)

########################################################################################################
#################################### III> IMPORT THE DATASET  ##########################################
########################################################################################################

dataset = read.csv('E:\\DESK PROJECTS\\MACHINE LEARNING SUMMARY\\ML DATASETS\\50_Startups.csv')  
summary(dataset)

########################################################################################################
################################### IV> ENCODING THE CATEGORICAL DATA ##################################
########################################################################################################

dataset$State = factor(dataset$State,
                       levels = c('New York', 'California', 'Florida'),
                       labels = c(1, 2, 3))

########################################################################################################
################################### V> SPLIT THE DATASET INTO TRAIN SET AND TEST SET ###################
########################################################################################################

split = sample.split(dataset$Profit, SplitRatio = 0.8)
training_set = subset(dataset, split == TRUE)
test_set = subset(dataset, split == FALSE)

########################################################################################################
################################## VI> FIT/TRAIN ML MODEL TO THE TRAINING SET ##########################
########################################################################################################

regressor = lm(formula = Profit ~ ., data = training_set)                                                      

########################################################################################################
################################### VII> VISUALIZE TEST SET  ###########################################
########################################################################################################

y_pred = predict(regressor, newdata = test_set)                                                          
y_pred

########################################################################################################
################################### VIII> BUILD THE OPTIMAL MODEL USING BACKWARD ELIMINATION  ##########
########################################################################################################

regressor = lm(formula = Profit ~ R.D.Spend + Administration + Marketing.Spend + State, data = dataset)
summary(regressor)
regressor = lm(formula = Profit ~ R.D.Spend + Administration + Marketing.Spend, data = dataset)
summary(regressor)
regressor = lm(formula = Profit ~ R.D.Spend + Marketing.Spend, data = dataset)
summary(regressor)
regressor = lm(formula = Profit ~ R.D.Spend,data = dataset)
summary(regressor)



